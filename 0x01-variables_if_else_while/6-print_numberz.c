#include <stdio.h>
/* betty style doc for function main goes there */
/**
 * main - Entry point for the program
 *
 * This function generates lowercase alphabets, excluding 'e' and 'o'
 *
 * Return: Always 0 (success)
 */
int main(void)
{
char ch;
for (ch = '0'; ch <= '9'; ch++)
{
putchar(char);
}
putchar('\n');
return 0;
}
